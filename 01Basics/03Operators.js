// In JS we have 4 operators
// Add, Subtract, Multiplication, Division, Modulus, Greatter than, lesser than
// + - / * % < >

var num1 = 2;
var num2 = 5

// Addition
console.log("Addition: " , num1 + num2);

// Substraction
console.log("Subtraction: ", num2 - num1);

// Multiplication
console.log("Multiplication: ", num1 * num2);

// Division
console.log("Division: ", num2 / num1);

// Modulus
console.log("Modulus: ", num2 % num1);

// Greater
console.log("Num2 is greater than num1: ", num2 > num1);

// Smaller
console.log("Num1 is smaller than num2: ", num1 < num2);